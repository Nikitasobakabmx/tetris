package lab.cars;

public class Iterator {
    public int id;
    public String name;
    public String studio_name;
    public String cost;
    public String photo;
    public String description;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCost() {
        return cost;
    }

    public String getPhoto() {
        return photo;
    }

    public String getDescription() {
        return description;
    }

    public String getStudio_name() {
        return studio_name;
    }
}
